#include <stdlib.h>
#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*str;
	int		buf;
	int		i;

	i = 0;
	if (s == NULL || f == NULL)
		return (NULL);
	buf = ft_strlen(s) + 1;
	if (!(str = (char*)malloc(sizeof(char) * buf)))
		return (NULL);
	while (s[i])
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
